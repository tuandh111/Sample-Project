    void edit() {
        try {
            String manh = (String) tblGridView.getValueAt(this.index, 0);
            O_NguoiHoc model = dao.findById(manh);
            if (model != null) {
                this.setModel(model);
                this.setStatus(false);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void setModel(O_NguoiHoc model) {
        txtMaNH.setText(model.getMaNH());
        txtHoTen.setText(model.getHoTen());
        cboGioiTinh.setSelectedIndex(model.isGioiTinh() ? 0 : 1);
        txtNgaySinh.setText(model.getNgaySinh());
        txtDienThoai.setText(model.getDienThoai());
        txtEmail.setText(model.getEmail());
        txtGhiChu.setText(model.getGhiChu());
    }

    O_NguoiHoc getModel() {
        O_NguoiHoc model = new O_NguoiHoc();
        model.setMaNH(txtMaNH.getText());
        model.setHoTen(txtHoTen.getText());
        model.setGioiTinh(cboGioiTinh.getSelectedIndex() == 0);
        model.setNgaySinh(txtNgaySinh.getText());
        model.setDienThoai(txtDienThoai.getText());
        model.setEmail(txtEmail.getText());
        model.setGhiChu(txtGhiChu.getText());
        model.setMaNV(ShareHelper.USER.getMaNV());
        Date now = new Date();
        String ngay = now.toString();
        model.setNgayDK(ngay);
        return model;
    }

    void setStatus(boolean insertable) {
        txtMaNH.setEditable(insertable);
        btnInsert.setEnabled(insertable);
        btnUpdate.setEnabled(!insertable);
        btnDelete.setEnabled(!insertable);

        boolean first = this.index > 0;
        boolean last = this.index < tblGridView.getRowCount() - 1;
        btnFirst.setEnabled(!insertable && first);
        btnPrev.setEnabled(!insertable && first);
        btnLast.setEnabled(!insertable && last);
        btnNext.setEnabled(!insertable && last);
    }

    boolean checkForm() {

        if (txtMaNH.getText().equalsIgnoreCase("")) {
            DialogHelper.alert(this, "Mã người học không được bỏ trống ô");
            return false;
        }
        if (txtHoTen.getText().equalsIgnoreCase("")) {
            DialogHelper.alert(this, "Tên họ không được bỏ trống ô");
            return false;
        }
        if (txtDienThoai.getText().equalsIgnoreCase("")) {
            DialogHelper.alert(this, "Số điện thoại không được bỏ trống ô");
            return false;
        }
        try {
            Integer.parseInt(txtDienThoai.getText());
        } catch (NumberFormatException e) {
            DialogHelper.alert(this, "Số điện thoại phải là số");
            return false;
        }

        return true;
    }

